public class Tarjet {
   
    // ATRIBUTOS
    double credito;
    double pagoMensualidad;
    double tazaInteres;
    int puntosCompras;

    @Override
    public String toString() {
        return "Tarjet{" + "credito=" + credito + ", pagoMensualidad=" + pagoMensualidad + ", tazaInteres=" + tazaInteres + ", puntosCompras=" + puntosCompras + '}';
    }
}
